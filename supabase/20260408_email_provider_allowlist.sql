-- 3. Trigger function ------------------------------------------------------
create or replace function public.enforce_email_domain_allowlist()
returns trigger
language plpgsql
security definer
set search_path = public, pg_catalog
as $$
declare
  v_domain text;
begin
  -- Skip rows without an email (phone-only signups, anonymous users, etc.).
  if new.email is null or new.email = '' then
    return new;
  end if;

  v_domain := lower(split_part(new.email, '@', 2));

  if v_domain = '' then
    return new;
  end if;

  if not exists (
    select 1 from public.allowed_email_domains where domain = v_domain
  ) then
    raise exception 'Email provider % is not supported for signups.', v_domain
      using errcode = 'check_violation', hint = 'Use a mainstream provider like Gmail, Outlook, iCloud, or your ISP.';
  end if;

  return new;
end;
$$;

